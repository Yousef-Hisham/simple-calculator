first_number = float(input("enter first number: "))

# 2- input operation
operation = input("enter operation type: ")

# 3- input second number
second_number = float(input("enter second number: "))

# 4- print the result

if operation == "+":
    print(first_number + second_number)
elif operation == "-":
    print(first_number - second_number)
elif operation == "/":
    print(first_number / second_number)
elif operation =="*":
  print(first_number * second_number)
else:
  print("error")

